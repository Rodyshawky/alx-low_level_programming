#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - add function
 * @a: input number
 * @b: input number
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction function
 * @a: input number
 * @b: input number
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply function
 * @a: input number
 * @b: input number
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide function
 * @a: input number
 * @b: input number
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod function
 * @a: input number
 * @b: input number
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
